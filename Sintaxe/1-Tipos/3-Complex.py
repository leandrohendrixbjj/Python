""""
complex: é usado para representar números complexos, que são compostos por uma parte real 
e uma parte imaginária.
"""

# Tipo complex
data = 3 + 4j # Número complexo com parte real 3 e imaginária 4

print(f"Real: {data.real}") # Saída: 3.0
print(f'Imaginaria: {data.imag}') # Saída: 4.0

print('Tipo:', type(data)) # Saída: <class 'complex'>
