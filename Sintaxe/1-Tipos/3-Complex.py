# Tipo complex
data = 3 + 4j # Número complexo com parte real 3 e imaginária 4

print('Real:', data.real) # Saída: 3.0
print('Imaginaria:', data.imag) # Saída: 4.0

print('Tipo:', type(data)) # Saída: <class 'complex'>
