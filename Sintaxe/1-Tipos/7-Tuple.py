"""
Tuplas: são tipos de dados imutáveis e ordenados. Elas são usadas para armazenar uma coleção 
de itens, podendo ser de tipos diferentes, como inteiros, strings ou até outras tuplas. 
O principal diferencial das tuplas em relação às listas é que elas são imutáveis, 
ou seja, uma vez criadas, não podem ser modificadas 
(não é possível adicionar, remover ou alterar elementos).
"""

data = (10, "Python", 3.14, True)

print('Tipo:',type(data)) # Saída: <class 'tuple'>
